#!/usr/bin/env bash

# Wires private OpenTelemetry Collector components from cd.splunkdev.com/gdi/unified-collector
# into this repo's otelcol build, driven entirely by the PRIVATE_COMPONENTS pipeline variable.
#
# This lets a build pull in a different set/version of private components without any change
# to this repo: the components are listed in PRIVATE_COMPONENTS and this script does the rest.
#
# PRIVATE_COMPONENTS format: whitespace/newline-separated entries of
#   <kind>/<subpath>@<version>[:key=value...]
# where <kind> is one of receiver, processor, exporter, extension, connector, and <subpath> is
# the module path under cd.splunkdev.com/gdi/unified-collector, e.g.:
#   exporter/s2sexporter@v0.1.0
#   processor/linebreakprocessor@v0.1.0
#   receiver/wineventlogreceiver@v0.1.0
#
# Optional overrides (rarely needed, for components that don't follow OTel conventions):
#   :pkg=<packageName>   package name declared by the module, if it differs from the last
#                        path segment of <subpath>
#   :ctor=<Func>         factory constructor function name, if not NewFactory
#
# What this script does:
#   1. `go get`s each module into the root go.mod/go.sum.
#   2. Generates internal/components/components_private_gen.go (NOT committed), which
#      registers the private factories via the additional* hooks in
#      internal/components/additional.go.
#   3. Runs `go mod tidy`.
#
# Requires GOPRIVATE / git credentials for cd.splunkdev.com to already be configured by the
# caller (see the compile-private job in .gitlab-ci.yml).

set -euo pipefail

MODULE_ROOT="cd.splunkdev.com/gdi/unified-collector"

SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
REPO_DIR="$( cd "${SCRIPT_DIR}/.." && pwd )"
GEN_FILE="${REPO_DIR}/internal/components/components_private_gen.go"

PRIVATE_COMPONENTS="${PRIVATE_COMPONENTS:-}"

if [[ -z "${PRIVATE_COMPONENTS// /}" ]]; then
  echo ">>> PRIVATE_COMPONENTS is empty, nothing to wire."
  exit 0
fi

declare -A KIND_TO_FIELD=(
  [receiver]="additionalReceivers"
  [processor]="additionalProcessors"
  [exporter]="additionalExporters"
  [extension]="additionalExtensions"
  [connector]="additionalConnectors"
)

imports=()
init_lines=()
summary=()

for entry in ${PRIVATE_COMPONENTS}; do
  [[ -z "$entry" ]] && continue

  # Split off any :key=value overrides.
  spec="$entry"
  overrides=""
  if [[ "$entry" == *:* ]]; then
    spec="${entry%%:*}"
    overrides="${entry#*:}"
  fi

  # spec is now <kind>/<subpath>@<version>
  if [[ "$spec" != */*@* ]]; then
    echo "ERROR: invalid PRIVATE_COMPONENTS entry '$entry' (expected <kind>/<subpath>@<version>)" >&2
    exit 1
  fi

  version="${spec##*@}"
  kind_subpath="${spec%@*}"
  kind="${kind_subpath%%/*}"
  subpath="${kind_subpath#*/}"

  if [[ -z "${KIND_TO_FIELD[$kind]+set}" ]]; then
    echo "ERROR: unknown component kind '$kind' in entry '$entry' (must be one of: ${!KIND_TO_FIELD[*]})" >&2
    exit 1
  fi
  if [[ -z "$subpath" || -z "$version" ]]; then
    echo "ERROR: invalid PRIVATE_COMPONENTS entry '$entry'" >&2
    exit 1
  fi

  pkg="${subpath##*/}"
  ctor="NewFactory"

  if [[ -n "$overrides" ]]; then
    for kv in ${overrides//:/ }; do
      key="${kv%%=*}"
      value="${kv#*=}"
      case "$key" in
        pkg) pkg="$value" ;;
        ctor) ctor="$value" ;;
        mod) : ;; # module path override not needed while everything lives under MODULE_ROOT
        *)
          echo "ERROR: unknown override '$key' in entry '$entry'" >&2
          exit 1
          ;;
      esac
    done
  fi

  module_path="${MODULE_ROOT}/${kind_subpath}"
  alias="priv_${pkg}"
  field="${KIND_TO_FIELD[$kind]}"

  echo ">>> Fetching ${module_path}@${version} ..."
  (cd "$REPO_DIR" && go get "${module_path}@${version}")

  imports+=("	${alias} \"${module_path}\"")
  init_lines+=("	${field} = append(${field}, ${alias}.${ctor}())")
  summary+=("${kind}/${subpath}@${version} -> ${field} (${alias}.${ctor}())")
done

echo ">>> Generating ${GEN_FILE} ..."
{
  echo "// Code generated by .gitlab/build-private-components.sh. DO NOT EDIT."
  echo "// This file is not committed; it only exists for builds that set PRIVATE_COMPONENTS."
  echo
  echo "package components"
  echo
  echo "import ("
  printf '%s\n' "${imports[@]}"
  echo ")"
  echo
  echo "func init() {"
  printf '%s\n' "${init_lines[@]}"
  echo "}"
} > "$GEN_FILE"

if command -v gofmt &> /dev/null; then
  gofmt -w "$GEN_FILE"
fi

echo ">>> Running go mod tidy ..."
(cd "$REPO_DIR" && go mod tidy)

echo ">>> Wired private components:"
printf '  - %s\n' "${summary[@]}"
